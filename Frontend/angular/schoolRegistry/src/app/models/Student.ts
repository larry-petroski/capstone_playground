export class Student {

  constructor(
    id: number, 
    studentName: string, 
    parentPhone: string, 
    parentEmail: string, 
    avatar: string
) {
    this.id = id
    this.studentName = studentName
    this.parentPhone = parentPhone
    this.parentEmail = parentEmail
    this.avatar = avatar
  }
    id: number;
    studentName: string;
    parentPhone: string;
    parentEmail: string;
    avatar: string;


}